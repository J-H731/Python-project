print("Using Lists")
listVar = [3.14, 25, 'Virtue', 'Stars']
print(listVar [0])
#shows first element in the list

print(listVar [1])
#shows second element in the list

print(listVar [len(listVar) - 1])
#shows last element in the list

listVar.append('Right')
#adds element to the end of the list

print(listVar [len(listVar)-1])
#displays new final element of the list

print(listVar [0:])
#displays all elements from the first element onward in the list

print(listVar [:4])
#displays all elements up to 5th element of the list
